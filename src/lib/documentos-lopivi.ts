// Funciones de utilidad
export function obtenerDocumentoPorId(id: string): DocumentoLOPIVI | undefined {
  return DOCUMENTOS_LOPIVI.find(doc => doc.id === id)
}

export function obtenerDocumentosEsenciales(): DocumentoLOPIVI[] {
  return DOCUMENTOS_LOPIVI.filter(doc => doc.esencial)
}

export function obtenerDocumentosPorCategoria(categoria: string): DocumentoLOPIVI[] {
  return DOCUMENTOS_LOPIVI.filter(doc => doc.categoria === categoria)
}

